-- 對應 ACCT-INT.cbl 使用的資料表
-- 脫敏範例，僅供 GHCP workshop 教學用

CREATE TABLE ACCOUNT (
    ACCOUNT_ID      CHAR(14)        NOT NULL,
    CUSTOMER_ID     CHAR(20)        NOT NULL,
    ACCT_TYPE       CHAR(2)         NOT NULL,   -- NS=一般活儲, SA=薪轉戶
    CURRENCY        CHAR(3)         NOT NULL,   -- TWD/USD/...
    BALANCE         DECIMAL(15, 2)  NOT NULL,   -- 可為負值代表透支
    STATUS          CHAR(1)         NOT NULL,   -- A=Active, F=Frozen, C=Closed
    OPEN_DATE       DATE            NOT NULL,
    LAST_INT_DATE   INTEGER,                    -- 最後計息日 YYYYMMDD
    CONSTRAINT PK_ACCOUNT PRIMARY KEY (ACCOUNT_ID)
);

CREATE INDEX IDX_ACCT_STATUS_CCY ON ACCOUNT (STATUS, CURRENCY);

CREATE TABLE INT_LOG (
    LOG_SEQ         BIGINT          NOT NULL GENERATED ALWAYS AS IDENTITY,
    ACCOUNT_ID      CHAR(14)        NOT NULL,
    RUN_YYMM        CHAR(6)         NOT NULL,
    INT_AMOUNT      DECIMAL(13, 2)  NOT NULL,
    OVERDRAFT_FEE   DECIMAL(13, 2)  NOT NULL,
    RATE_APPLIED    DECIMAL(7, 6)   NOT NULL,
    ACCT_TYPE       CHAR(2)         NOT NULL,
    CREATED_AT      TIMESTAMP       DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT PK_INT_LOG PRIMARY KEY (LOG_SEQ)
);

CREATE INDEX IDX_INT_LOG_RUN ON INT_LOG (RUN_YYMM, ACCOUNT_ID);

